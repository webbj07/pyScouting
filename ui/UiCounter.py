import UiElement
class UiCounter(UiElement):
    # local varables:
    # type of Tk ui element
    # positive and negative clicking buttons
    # display data for that ui element
    
    def __init__(self):
        pass
    def display(self):
        pass
    def handlePositiveClick(self):
        self.data += 1
    def handleNegativeClick(self):
        self.data -= 1


    


